package Arrays;

public class ArrayPractice {
    public static void main(String[] args) {

        String[] cars = new String[3];

        cars[0] = "honda";
        cars[1] = "Mitsubishi";
        cars[2] = "toyota";

        for (int i = 0; i < 3; i++){
            System.out.println(cars[i]);
        }

    }
}
